package com.cdd.util;

public class EmployeePay {
    private int id;
    private String eName;
    private String oldEarning;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String geteName() {
        return eName;
    }
    public void seteName(String eName) {
        this.eName = eName;
    }
    public String getOldEarning() {
        return oldEarning;
    }
    public void setOldEarning(String oldEarning) {
        this.oldEarning = oldEarning;
    }
}
