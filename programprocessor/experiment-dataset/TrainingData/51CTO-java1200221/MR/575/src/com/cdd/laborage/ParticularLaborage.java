package com.cdd.laborage;

public class ParticularLaborage {
    private int id;
    private String name;
    private float base;
    private float subsidy;
    private float deduct;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public float getBase() {
        return base;
    }
    public void setBase(float base) {
        this.base = base;
    }
    public float getSubsidy() {
        return subsidy;
    }
    public void setSubsidy(float subsidy) {
        this.subsidy = subsidy;
    }
    public float getDeduct() {
        return deduct;
    }
    public void setDeduct(float deduct) {
        this.deduct = deduct;
    }
    
}
