package com.cdd.util;

public class StuInfo {
    private int id;
    private String sName;
    private String sSex;
    private String sBrithday;
    private String sSpeciality;
    private String sAddress;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getsName() {
        return sName;
    }
    public void setsName(String sName) {
        this.sName = sName;
    }
    public String getsSex() {
        return sSex;
    }
    public void setsSex(String sSex) {
        this.sSex = sSex;
    }
    public String getsBrithday() {
        return sBrithday;
    }
    public void setsBrithday(String sBrithday) {
        this.sBrithday = sBrithday;
    }
    public String getsSpeciality() {
        return sSpeciality;
    }
    public void setsSpeciality(String sSpeciality) {
        this.sSpeciality = sSpeciality;
    }
    public String getsAddress() {
        return sAddress;
    }
    public void setsAddress(String sAddress) {
        this.sAddress = sAddress;
    }
    
}
